import express from 'express';
import { followUserController, unfollowUserController, getFollowersController, getFollowingController } from '../controllers/follow.controller';

const router = express.Router();

router.post('/follow', followUserController);
router.post('/unfollow', unfollowUserController);
router.get('/followers/:userId', getFollowersController);
router.get('/following/:userId', getFollowingController);

export default router;
